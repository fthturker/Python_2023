#And operatoru
print(1<2) #True
print("Murat"=="Murat") #True

print(1<2 and "Murat"=="Murat") #True
print(1<2 and "Murat"=="Fatih") #False

#Or operatoru
print(1<2 or "Murat"=="Fatih") #True

#Not operatoru
print(not 2==2) #False

